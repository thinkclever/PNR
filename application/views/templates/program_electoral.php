<strong>Transparență </strong>– fiecare instituție publică este în slujba cetățeanului și finanțată de cetățean. Cheltuielile, activitățile și modul de lucru trebuie să fie transparent.

&nbsp;

<strong>Dreptate</strong> - toți vrem dreptate, o justiție unică, aplicabilă pentru toți. Legea să fie aplicată în mod egal și nepărtinitor. Să avem toți parte de dreptate, indiferent de statutul social.

&nbsp;

<strong>Copii - </strong>propunem un sistem educațional modern, care să includă sportul ca ramură importantă a educării populației, dublat de materiile vocaționale și de dezvoltarea personală.

&nbsp;

<strong>Muncă -</strong> dacă avem loc de muncă, vom avea bani să ne putem hrăni sănătos, să putem întemeia o familie, să ne educăm, să evoluăm.

&nbsp;

<strong>Mediu</strong> - Grija pentru mediul înconjurător reprezintă de fapt polița de asigurare pentru o viață sănătoasă a populației. Industrializarea necontrolată, defrișările masive, poluarea excesivă au făcut ca mediul înconjurător să aibă de suferit și, drept urmare, populația suportă consecințele finale.

&nbsp;

<strong>Infrastructură</strong> – dacă avem drumuri, străzi, autostrăzi, putem circula repede și în siguranță. Ne putem dezvolta economic, turistic, social.

&nbsp;

<strong>Sănătate</strong> - dacă avem unități medicale la standarde europene în fiecare localitate, ne putem trata eficient în propria țară.

&nbsp;

<strong>Cultura </strong>- face parte integrantă din istoria unui popor. Prin intermediul culturii, o nație își prezintă bogățiile lumii întregi.

&nbsp;

<strong>Apărare</strong>  - În contextul geopolitic actual, apărarea națională reprezintă un factor cheie în strategia de securitate a României, iar calitatea de membru NATO ne conferă o siguranță în acest sens.

&nbsp;

<strong>Economie </strong>– Economia României poate fi competitivă la nivel european dacă legislația națională este simplă și stabilă, iar sistemul de impozitare  este unul flexibil, care să nu devină un impediment pentru întreprinzători.

