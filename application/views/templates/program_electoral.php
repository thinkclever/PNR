Serviciile Secrete din România nu vor mai avea dreptul să desfășoare activități economice.

&nbsp;

Partidele politice care acordă foloase necuvenite alegătorilor (bunuri, alimente, bani) pentru a obține voturi, cât și cele care fraudează scrutinul electoral la care participă, vor fi descalificate din cursa electorală.

